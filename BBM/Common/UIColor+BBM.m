#import "UIColor+BBM.h"

@implementation UIColor (BBM)

+ (UIColor *)BBMLightBlueColor
{
    return [UIColor colorWithRed:0 green:166/255.0f blue:221/255.0f alpha:1];
}

+ (UIColor *)BBMYellowColor
{
    return [UIColor colorWithRed:236/255.0f green:189/255.0f blue:23/255.0f alpha:1];
}

+ (UIColor *)BBMGreenColor
{
    return [UIColor colorWithRed:165/255.0f green:192/255.0f blue:34/255.0f alpha:1];
}

+ (UIColor *)BBMGrayColor
{
    return [UIColor colorWithRed:188/255.0f green:188/255.0f blue:188/255.0f alpha:1];
}

@end
